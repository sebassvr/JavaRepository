/* Cycles
 *  
 *      Ciclo do while    
 *  
 *      do{
 *          Instructions
 *          Instructions
 *      }while(condition);
 *  
 * 
 * 
 */


public class Three_cycle_do_while {
    public static void main(String[] args) {
        int i=0;
        do{
            i++;
            System.out.println(i);
        }while(i<10);




    }
}
